   @Override
   public void afterLoad() {
      setupEvents();
      List<BSBreadcrumbable> elements = new ArrayList();
      BSBreadcrumbElement uno = new BSBreadcrumbElement("Uno", false);
      BSBreadcrumbElement dos = new BSBreadcrumbElement("Dos", false);
      BSBreadcrumbElement tres = new BSBreadcrumbElement("Tres", false);
      BSBreadcrumbElement cuatro = new BSBreadcrumbElement("Cuatro", false);
      elements.add(uno);
      elements.add(dos);
      elements.add(tres);
      elements.add(cuatro);
      elements.add(new BSBreadcrumbElement("Tres.1", tres, true));
      elements.add(new BSBreadcrumbElement("Tres.2", tres, true));
      elements.add(new BSBreadcrumbElement("Cuatro.1", cuatro, true));
      elements.add(new BSBreadcrumbElement("Cuatro.2", cuatro, true));
      elements.add(new BSBreadcrumbElement("Uno.1", uno, true));
      elements.add(new BSBreadcrumbElement("Uno.2", uno, true));
      breadCrumb.setElements(elements);


      breadCrumb.setCurrentBGPaint(new GradientPaint(0,
                                                     0,
                                                     new Color(200, 255, 75),
                                                     0,
                                                     30,
                                                     new Color(191, 250, 55)));
      breadCrumb.setFutureBGPaint(new GradientPaint(0, 0,
                                                    new Color(28, 163, 218),
                                                    0, 20, new Color(38, 173,
                                                                     228)));
      breadCrumb.setPastBGPaint(new GradientPaint(0, 0, new Color(120, 121, 115),
                                                  0, 30,
                                                  new Color(130, 131, 126)));
   }

